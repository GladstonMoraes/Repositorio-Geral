/*
* extrurura de controle com repetição DETERMINADA
*No "for" a sintaxe é diferente, dentro dos parênteses se espera a declaração da variável seguido por ponto e vírgula
*depois a expressão que retornará true or false seguido por ponto e vírgula e por fim o encremento ou decremento para a limitar a contagem da expressão
*/



for (let controle = 1; controle <= 20; controle ++) {
    console.log(controle);
}

console.log("Fim!");